int	ft_any(char **tab, int (*f)(char*))
{
	int i;
	int trigger;

	i = -1;
	trigger = 0;
	while (tab[++i] != 0)
	{
		if (f(tab[i]) == 1)
			trigger = 1;
	}
	return (trigger);
}
