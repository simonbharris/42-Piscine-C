int	ft_str_is_lowercase(char *str);

int	ft_str_is_lowercase(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (!(str[i] >= 'a' && str[i] <= 'z'))
			return (0);
		if (str[i] == '\0')
			return (1);
		i++;
	}
	return (1);
}
